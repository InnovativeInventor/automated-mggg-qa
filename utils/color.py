    PURPLE = "\033[105m"
    CYAN = "\033[106m"
    DARKCYAN = "\033[46m"
    BLUE = "\033[104m"
    GREEN = "\033[102m"
    YELLOW = "\033[103m"
    RED = "\033[101m"
